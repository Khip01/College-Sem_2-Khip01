public class DataMahasiswa {

    public String nama, nim;
    public char jenisKelamin;
    public float ipk;    


    public DataMahasiswa(String nama, String nim, char jenisK, float ipk) {
        this.nama = nama;
        this.nim = nim;
        this.jenisKelamin = jenisK;
        this.ipk = ipk;
    }
    
}
